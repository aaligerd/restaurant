package com.enterprise.restaurant.model;

public class PersonalizeHttpStatus {
    private int status;
    private String msg;

    public PersonalizeHttpStatus(int status, String msg) {
        this.status = status;
        this.msg = msg;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
